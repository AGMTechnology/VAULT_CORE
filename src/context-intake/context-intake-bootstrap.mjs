import fs from "node:fs";
import path from "node:path";

function toYamlList(values, indentation = "  ") {
  if (!values || values.length === 0) {
    return `${indentation}[]`;
  }
  return values.map((value) => `${indentation}- "${String(value).replaceAll('"', '\\"')}"`).join("\n");
}

function formatTimestampIso() {
  return new Date().toISOString();
}

function writeFile(filePath, content) {
  fs.writeFileSync(filePath, content, "utf8");
}

export function generateBootstrapFolder(workspacePath, payload) {
  const bootstrapRoot = path.join(workspacePath, ".vault-core");
  fs.mkdirSync(bootstrapRoot, { recursive: true });

  const projectContextContent = [
    "version: 1",
    `generatedAt: "${formatTimestampIso()}"`,
    `projectId: "${payload.projectId}"`,
    `sourceType: "${payload.source.type}"`,
    `workspacePath: "${workspacePath.replaceAll("\\", "\\\\")}"`,
    "stack:",
    "  languages:",
    toYamlList(payload.analysis.languages, "    "),
    "  frameworks:",
    toYamlList(payload.analysis.frameworks, "    "),
    "  packageManagers:",
    toYamlList(payload.analysis.packageManagers, "    "),
    "  testTooling:",
    toYamlList(payload.analysis.testTooling, "    "),
    "  ciFiles:",
    toYamlList(payload.analysis.ciFiles, "    "),
    "docs:",
    toYamlList(payload.analysis.docs, "  "),
  ].join("\n");

  const rulesContent = [
    "version: 1",
    "rules:",
    "  - id: assigned-agent-only",
    "    severity: blocker",
    "    description: \"Only assigned agent can start execution.\"",
    "  - id: tdd-required",
    "    severity: blocker",
    "    description: \"Tests must be written first, then implementation.\"",
    "  - id: docs-reviewed-before-start",
    "    severity: error",
    "    description: \"Required docs must be reviewed before moving to in-progress.\"",
    "  - id: central-memory-only",
    "    severity: blocker",
    "    description: \"Use VAULT_CORE memory hub endpoint only.\"",
    "memory:",
    "  endpoint: \"/api/memory\"",
  ].join("\n");

  const recommendedSkills = [];
  if (payload.analysis.frameworks.includes("Next.js")) {
    recommendedSkills.push("nextjs-app-router");
  }
  if (payload.analysis.frameworks.includes("React")) {
    recommendedSkills.push("react-ui");
  }
  if (payload.analysis.testTooling.includes("Vitest")) {
    recommendedSkills.push("vitest-testing");
  }
  recommendedSkills.push("ticket-workflow", "memory-postmortem");

  const skillsContent = [
    "version: 1",
    "skills:",
    ...recommendedSkills.map((skill) => `  - id: "${skill}"`),
  ].join("\n");

  const docsIndexContent = [
    "# Docs Index",
    "",
    "Generated by VAULT_CORE context intake.",
    "",
    "## Detected docs",
    ...payload.analysis.docs.map((docPath) => `- \`${docPath}\``),
    "",
    "## Required VAULT docs",
    "- `docs/ai/VAULT_CORE_TECH_SPEC.md`",
    "- `docs/ai/adr/ADR-0001-vault-core-architecture.md`",
    "- `docs/ai/VAULT_CORE_STACK_MATRIX.md`",
    "- `docs/ai/VAULT_CORE_MIGRATION_ROADMAP.md`",
    "- `docs/ai/contracts/vault-core-contract-v1.schema.json`",
  ].join("\n");

  const contractTemplateContent = [
    "# Contract Template",
    "",
    "## Scope",
    "- Objective:",
    "- In scope:",
    "- Out of scope:",
    "",
    "## Acceptance criteria",
    "-",
    "",
    "## Test plan",
    "- Nominal:",
    "- Failure:",
    "",
    "## Context signals",
    `- Languages: ${payload.analysis.languages.join(", ") || "n/a"}`,
    `- Frameworks: ${payload.analysis.frameworks.join(", ") || "n/a"}`,
    `- Test tooling: ${payload.analysis.testTooling.join(", ") || "n/a"}`,
    "",
    "## Memory reminders",
    "- Include contextual lessons from VAULT_CORE Memory Hub before publication.",
  ].join("\n");

  const executionChecklistContent = [
    "# Agent Execution Checklist",
    "",
    "- [ ] Read mandatory docs and project rules.",
    "- [ ] Validate assignee identity.",
    "- [ ] Write failing tests first.",
    "- [ ] Implement minimal changes to pass tests.",
    "- [ ] Add `[DEV_DONE]` evidence before in-review.",
    "- [ ] Push lessons to VAULT_CORE Memory Hub.",
  ].join("\n");

  const files = [
    {
      relativePath: ".vault-core/project.context.yaml",
      content: `${projectContextContent}\n`,
    },
    {
      relativePath: ".vault-core/rules.global.yaml",
      content: `${rulesContent}\n`,
    },
    {
      relativePath: ".vault-core/skills.recommended.yaml",
      content: `${skillsContent}\n`,
    },
    {
      relativePath: ".vault-core/docs.index.md",
      content: `${docsIndexContent}\n`,
    },
    {
      relativePath: ".vault-core/contract.template.md",
      content: `${contractTemplateContent}\n`,
    },
    {
      relativePath: ".vault-core/agent.execution.checklist.md",
      content: `${executionChecklistContent}\n`,
    },
  ];

  for (const file of files) {
    writeFile(path.join(workspacePath, file.relativePath), file.content);
  }

  return {
    root: ".vault-core",
    files: files.map((file) => file.relativePath),
  };
}
